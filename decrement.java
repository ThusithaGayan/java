class decrement{
	public static void main(String args[]){
		int x,y,a,b;
		a=10;
		b=10;

		x=a--;
		y=--b;

		//new value of a
		System.out.println("a="+a);
		//new value of x
		System.out.println("x="+x);
		//new value of b
		System.out.println("b="+b);
		//value of y
		System.out.println("y="+y);
	}
}